    /**
     * Controla el boton Listar
     *
     * @param evt
     */
    private void jbtn_listarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_listarActionPerformed

        if (!this.controlador.buscarOperadorPorDepartamentoTipoTramite(this.controlador.getNuevoTurno().getUnDepartamentoB()).isEmpty()) {
            //Actualizar el TableModel con la lista del controladorEmpleado
            this.tablaOperadorModelo.setOperadores(this.controlador.buscarOperadorPorDepartamentoTipoTramite(this.controlador.getNuevoTurno().getUnDepartamentoB()));

            System.out.println("hola");

            //Refrescar el modelo en la tabla
            this.tablaOperadorModelo.fireTableDataChanged();

            limpiarTodosLosCampos();
            habilitarTodosLosCampos(false);
            habilitarTodosLosBotones(false);

            this.validador.habilitarBoton(false, this.jbtn_siguiente, new Color(30, 132, 73), Color.WHITE, null, null);
            this.validador.habilitarBoton(true, this.jbtn_listar, new Color(30, 132, 73), Color.WHITE, null, null);
            this.validador.habilitarCampo(true, this.jtf_buscarEmpleado);

            this.operadorSeleccionado = null;
        }else{
            System.out.println("no existen datos");
        }


    }//GEN-LAST:event_jbtn_listarActionPerformed

