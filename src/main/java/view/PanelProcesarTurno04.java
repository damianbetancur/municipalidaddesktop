    /**
     * Carga los tipos de persona el el JComboBox tipo de persona
     */
    public void cargarTiposDePersona() {
        DefaultComboBoxModel mdl = new DefaultComboBoxModel((Vector) this.controlador.buscarTodosLosTiposDeEmpleados());
        this.jcb_tipoEmpleado.setModel(mdl);
    }

    /**
     * Selecciona una fila de la tabla, devolviendo un objeto Persona
     */
    public void seleccionarPersona() {
        //obtiene el indice de la fila seleccionada en la tabla de operadores
        int filaSeleccionada = this.jtb_empleado.getSelectedRow();
        // si la fila esta seleccionada, seteamos la persona auxiliar, llamando al modelo de tabla
        if (filaSeleccionada >= 0) {
            this.operadorSeleccionado = this.tablaOperadorModelo.obtenerOperadorEn(filaSeleccionada);
            this.jtf_nombre.setText(this.operadorSeleccionado.getNombre());
            this.jtf_apellido.setText(this.operadorSeleccionado.getApellido());
            this.jtf_dni.setText(this.operadorSeleccionado.getDni());
            this.jcb_tipoEmpleado.removeAllItems();
            this.jcb_tipoEmpleado.addItem(this.operadorSeleccionado.getUnTipoOperador().getDescripcion());
            this.validador.habilitarBoton(true, this.jbtn_siguiente, new Color(30, 132, 73), Color.WHITE, null, null);
        } else {

        }
    }

